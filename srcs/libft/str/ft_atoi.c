#include "../libft.h"

int	ft_atoi(const char *nptr)
{
	int i;
	int b;
	int n;

	n = 0;
	i = 0;
	b = 1;
	while (((nptr[i] >= 8 && nptr[i] <= 13) || nptr[i] == 32))
		i++;
	if ((nptr[i] == '+' || nptr[i] == '-'))
	{
		if (nptr[i] == '-')
			b = -1;
		i++;
	}
	while ((nptr[i] >= '0' && nptr[i] <= '9'))
	{
		n = (n * 10) + (nptr[i] - 48);
		i++;
	}
	return ((int)(n * b));
}
